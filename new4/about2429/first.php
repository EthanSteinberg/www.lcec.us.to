This is LCEC's fourth year.  The club initially set out to stimulate, inspire, and enrich the science, mathematics, and technological aspects of education for the students in our school. We still hold the same goal. Except, we've grown. A lot. We now reach all the schools in our district, and we're pouring out to neigboring cities aswell as we try our best to spread engineering to our community.
<br><br>
<b>FRC-</b>
<br>
Our FRC program has been around since the beginning. In our first year, we recieved the Rookie All Star Award in the Los Angeles Regional. Our Second year, we returned to the LA regional with a succesful robot. In year three, we expanded to the Las Vegas Regional, where we won the Enginnering Inspiration Award. This sent us to Atlanta for the second time, where we became finalists in our division. As we proceede through year four, we are trying to win Chairman's and get to championship once more.
<br><br>
<b>FLL-</b>
<br>
Our FLL program has existed for three years now. Each of our teams builds a fully autonomous robot that completes varoius tasks on a small 4x8 foot feild. We started out with one team. The Golden Circut Breakers made it to Legoland's regional championship. This 6th grade team graduated into a new team at our middle school, and we also added one more team to another elementry school. In our third year, we spread out with two more teams to come to our current size. Four of our five teams moved on to championships, carrying several of awards including Teamwork and Robot Design. We also received best youth mentor. At championships, one of our elementary teams was awarded first place project. In our off season, we also bring lego robotics to Jackson Elementary School.
<br><br>
<b>Solar Boat-</b>
<br>
Solar Boat has been part of our club for three years. Each year, we build a new boat that runs on solar power. It is raced for both enduracne and sprint. The Solar Boat program is coordinated by the Metropolitain Water District and promotes green power and water conservation. In our first year at solar boat, we were honored with second place rookie techinical report and 2nd place rookie sprint and endurance. In last year's competiton, we also received 3rd place PSA, 2nd place tech, and the Teamwork award. Our third year has already started, and we are looking forward to  another fun season of Solar Boat.
<br><br>
<b>Mate-</b>
<br>
Mate, or Marine Advanced Technology Education, is a program where students from all ages compete to build an underwater, teleoperated robot that acomplishes various tasks. Mate has been a part of our mentored program for two years. Our Middle school team was first place scout class last year, competing directly with veteran teams. This year's season has already started. We are anticipating three competative teams: one on the easier scout class and two on harder ranger class.
